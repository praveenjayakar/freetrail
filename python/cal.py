
def add(num1, num2):
    value = num1 + num2
    return value

def sub(num1, num2):
    value = num1 - num2
    return value

def mul(num1 , num2):
    value = num1 * num2
    return value

def div(num1, num2):
    value = num1 / num2
    return value


print(add(5, 5))

print(sub(5, 5))

print(mul(10, 10))

print(div(500, 200))
